#include <iostream>

/*
=========================================================
  СТРУКТУРА ЭЛЕМЕНТА СТЕКА
  Каждый контейнер хранится как узел односвязного списка.
=========================================================
*/
struct Node {
    int type;      // Вид товара в контейнере
    Node* next;    // Указатель на элемент ниже
};

/*
=========================================================
  СТРУКТУРА СТЕКА
  top — указатель на верхний контейнер
=========================================================
*/
struct Stack {
    Node* top;
};

/*
=========================================================
  ФУНКЦИЯ PUSH
  Добавляет контейнер на вершину стопки.
=========================================================
*/
void Push(Stack& s, int value) {
    Node* node = new Node;   // создаём новый элемент
    node->type = value;      // записываем вид товара
    node->next = s.top;      // новый элемент указывает на старую вершину
    s.top = node;            // вершина теперь — новый элемент
}

/*
=========================================================
  ФУНКЦИЯ POP
  Удаляет верхний контейнер и возвращает его вид.
  Предполагается, что стек не пуст.
=========================================================
*/
int Pop(Stack& s) {
    Node* temp = s.top;      // сохраняем текущую вершину
    int value = temp->type;  // запоминаем вид товара
    s.top = temp->next;      // сдвигаем вершину вниз
    delete temp;             // освобождаем память
    return value;
}

/*
=========================================================
  ФУНКЦИЯ TOP
  Возвращает вид верхнего контейнера без удаления.
=========================================================
*/
int Top(Stack& s) {
    return s.top->type;
}

/*
=========================================================
  ФУНКЦИЯ EMPTY
  Проверяет, пуста ли стопка.
=========================================================
*/
bool Empty(Stack& s) {
    return s.top == nullptr;
}

/*
=========================================================
  ФУНКЦИЯ CLEAR
  Полностью очищает стопку и освобождает память.
=========================================================
*/
void Clear(Stack& s) {
    while (!Empty(s)) {
        Pop(s);
    }
}


/*
=========================================================
  ГЛАВНАЯ ФУНКЦИЯ
=========================================================
*/
int main() {

    int n = 0;         // количество видов товаров
    Stack stacks[501];  // массив стопок (нумерация с 1)
    std::cin >> n;

    // Инициализация всех стопок как пустых
    for (int i = 0; i <= 500; ++i) {
        stacks[i].top = nullptr;
    }

    /*
    =====================================================
      ОСОБЫЙ СЛУЧАЙ: n == 2
      Для двух стопок возможны ограничения.
    =====================================================
    */
    if (n == 2) {

        bool possible = true;   // можно ли решить задачу
        bool direction = true;  // направление перекладывания
        int moves = 0;          // количество операций

        // ===== Чтение первой стопки =====
        int count = 0;
        std::cin >> count;

        if (count > 0) {
            int value = 0;
            std::cin >> value;

            int i = 1;

            // пропускаем элементы вида 1
            while (value == 1 && i < count) {
                std::cin >> value;
                i++;
            }

            int wrong = (value == 2);

            while (i < count) {
                std::cin >> value;
                if (value == 1) {
                    possible = false;
                }
                wrong++;
                i++;
            }

            moves += wrong;
        }

        // ===== Чтение второй стопки =====
        std::cin >> count;

        if (count > 0) {
            int value = 0;
            std::cin >> value;

            int i = 1;

            while (value == 2 && i < count) {
                std::cin >> value;
                i++;
            }
            if (value == 1) {
                direction = false;
            }

            int wrong = (value == 1);

            while (i < count) {
                std::cin >> value;
                if (value == 2) {
                    possible = false;
                }
                wrong++;
                i++;
            }

            moves += wrong;
        }

        if (!possible) {
            std::cout << 0 << "\n";
        } else {
            for (int i = 0; i < moves; ++i) {
                if (direction) {
                    std::cout << "1 2\n";
                } else {
                    std::cout << "2 1\n";
                }
            }
        }

        // Очистка памяти
        for (int i = 0; i <= n; ++i) {
            Clear(stacks[i]);
        }
        return 0;
    }

    /*
    =====================================================
      ОБЩИЙ СЛУЧАЙ: n > 2
    =====================================================
    */

    // ===== Чтение данных =====
    for (int i = 1; i <= n; ++i) {
        int count = 0;
        std::cin >> count;

        for (int j = 0; j < count; ++j) {
            int value = 0;
            std::cin >> value;

            // Контейнеры, уже стоящие в правильной стопке,
            // снизу игнорируем
            if (value != i) {
                Push(stacks[i], value);
            }
        }
    }

    /*
    =====================================================
      ОСНОВНОЙ АЛГОРИТМ

      Для каждой стопки:
      - Если целевая стопка пуста — переносим напрямую.
      - Иначе используем последнюю стопку как буфер.
    =====================================================
    */

    for (int i = 1; i < n; ++i) {
        while (!Empty(stacks[i])) {
            int target = Top(stacks[i]);
            if (Empty(stacks[target])) {
                // Прямой перенос
                std::cout << i << " " << target << "\n";
                Pop(stacks[i]);
            } else {
                // Временный перенос в буфер
                std::cout << i << " " << n << "\n";
                Push(stacks[n], Pop(stacks[i]));
            }
        }
    }

    /*
    =====================================================
      Разгрузка буферной стопки
    =====================================================
    */
    while (!Empty(stacks[n])) {
        int value = Pop(stacks[n]);
        if (value != n) {
            std::cout << n << " " << value << "\n";
        }
    }

    /*
    =====================================================
      Финальная очистка памяти
    =====================================================
    */
    for (int i = 0; i <= n; ++i) {
        Clear(stacks[i]);
    }

    return 0;
}