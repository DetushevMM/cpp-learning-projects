/*
Фильтруем слова
Дано число k и список из n слов (строк, состоящих из латинских букв). Необходимо оставить только слова длины не меньше k, привести их к нижнему регистру и вывести результат.

Нужно использовать средства std::ranges для прохода по данным или применения алгоритма к диапазону. 
Можно предварительно сохранить входные данные в контейнер, а можно сразу работать с потоковым диапазоном. 
Запрещается использовать циклы со счетчиками/итераторами, создавать контейнеры для хранения промежуточных результатов, а также использовать рекурсию.

Формат ввода
В первой строке входных данных записаны целое число n (1≤n≤10^6) — количество слов, и целое число k (1≤k≤100) — минимальная длина слова. В следующих строках записаны слова, состоящие из латинских букв. 
Длина каждого слова не превышает 100 символов.

Формат вывода
Выведите отфильтрованные слова, приведенные к нижнему регистру, в порядке их следования во входных данных, по одному слову на строку.

Пример
Ввод                    Вывод
5 3                     abcd
AB                      dcba
A
aB
abcd
DcBA
*/

#include <iostream>
#include <ranges>
#include <algorithm>
#include <string>

namespace rng = std::ranges;
namespace vs = std::views;

int main() {
  std::ios::sync_with_stdio(false);
  std::cin.tie(nullptr);

  size_t n = 0;
  size_t k = 0;
  std::cin >> n >> k;

  auto to_lower = [](char c) { return (c >= 'A' && c <= 'Z') ? c + ('a' - 'A') : c; };

  for (size_t i = 0; i < n; ++i) {
    std::string word;
    std::cin >> word;

    if (word.size() < k) {
      continue;
    }

    rng::transform(word, word.begin(), to_lower);

    std::cout << word << '\n';
  }

  return 0;
}
